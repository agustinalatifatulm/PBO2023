public class Skripsipojo {
    private String idskripsi;
    private String judul;
    private String pengarang;
    private String tahun;
    private String halaman;

    /**
     * @return the idskripsi
     */
    public String getIdskripsi() {
        return idskripsi;
    }

    /**
     * @param idskripsi the idskripsi to set
     */
    public void setIdskripsi(String idskripsi) {
        this.idskripsi = idskripsi;
    }

    /**
     * @return the judul
     */
    public String getJudul() {
        return judul;
    }

    /**
     * @param judul the judul to set
     */
    public void setJudul(String judul) {
        this.judul = judul;
    }

    /**
     * @return the tahun
     */
    public String getTahun() {
        return tahun;
    }

    /**
     * @param tahun the tahun to set
     */
    public void setTahun(String tahun) {
        this.tahun = tahun;
    }

    /**
     * @return the halaman
     */
    public String getHalaman() {
        return halaman;
    }

    /**
     * @param halaman the halaman to set
     */
    public void setHalaman(String halaman) {
        this.halaman = halaman;
    }

    /**
     * @return the pengarang
     */
    public String getPengarang() {
        return pengarang;
    }

    /**
     * @param pengarang the pengarang to set
     */
    public void setPengarang(String pengarang) {
        this.pengarang = pengarang;
    }
    
}
